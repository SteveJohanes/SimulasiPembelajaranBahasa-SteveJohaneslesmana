package SimulasiPembelajaranBahasa;

public class BhsModel {

    private String kodebhs, namabhs, gambar, jenis, carabaca;

    public String getCarabaca() {
        return carabaca;
    }

    public void setCarabaca(String carabaca) {
        this.carabaca = carabaca;
    }

    public String getKodebhs() {
        return kodebhs;
    }

    public void setKodebhs(String kodebhs) {
        this.kodebhs = kodebhs;
    }

    public String getNamabhs() {
        return namabhs;
    }

    public void setNamabhs(String namabhs) {
        this.namabhs = namabhs;
    }

    public String getGambar() {
        return gambar;
    }

    public void setGambar(String gambar) {
        this.gambar = gambar;
    }

    public String getJenis() {
        return jenis;
    }

    public void setJenis(String jenis) {
        this.jenis = jenis;
    }

}
